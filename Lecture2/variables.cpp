
// Variables
#include <iostream>
using namespace std;

int main() {
    // Integer
    int age = 18;
    cout << "Age : " << age << endl;

    // print the size of integers(4)
    cout << sizeof(age) << endl;


    // Float
    float pi = 3.14;
    cout << "Pi : " << pi << endl;
    // print the size of floats(4)
    cout << sizeof(pi) << endl;



    return 0;
}