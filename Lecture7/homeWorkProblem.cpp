

// 6 & 10
// 6 | 10
// 6 ^ 10
// 10 << 2
// 10 >> 1

#include <iostream>
using namespace std;

int main() {
    // 6 & 10
    cout << (6& 10) << endl;

    // 6 | 10
    cout << (6 | 10) << endl;

    // 6 ^ 10
    cout << (6 ^ 10) << endl;

    // 10 << 2
    cout << (10 << 2) << endl;

    // 10 >> 1
    cout << (10 >> 1) << endl;

    return 0;

}